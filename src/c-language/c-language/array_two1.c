//[?] 이차원 배열 생성 및 반복문으로 출력하기
#include <stdio.h>

int main(void)
{
    // [1] 2차원 배열 선언: 2행 3열짜리 정수형 배열
    // intArray[행][열] 형태로 접근
    // 변수명에 'int' 접두사를 붙여 자료형이 정수(int)임을 명시하는 관례적인 스타일
    int intArray[2][3];

    // [2] 배열 요소에 직접 값을 대입 (초기화)
    // 첫 번째 행(0번째 행)에 값 설정
    intArray[0][0] = 1;
    intArray[0][1] = 2;
    intArray[0][2] = 3;

    // 두 번째 행(1번째 행)에 값 설정
    intArray[1][0] = 4;
    intArray[1][1] = 5;
    intArray[1][2] = 6;

    // [3] 2중 for문을 사용하여 배열의 모든 요소를 출력
    // 바깥쪽 for문은 행을 반복 (0부터 1까지)
    for (int i = 0; i < 2; i++)
    {
        // 안쪽 for문은 각 행의 열을 반복 (0부터 2까지)
        for (int j = 0; j < 3; j++)
        {
            // 현재 행(i)과 열(j)에 해당하는 값을 출력
            // '\t'는 탭(tab) 간격으로 정렬
            printf("%d\t", intArray[i][j]);
        }

        // 한 행을 모두 출력한 후 줄바꿈
        printf("\n");
    }

    // [4] 프로그램 정상 종료
    return 0;
}
