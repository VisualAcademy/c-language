void main(void)
{
	int num = 0;
	int i, j;
	while (1)
	{
		printf("0~27까지 번호입력 : _\b"); scanf("%d", &num);

		if (num < 0 || num > 27)
		{
			break;
		}

		for (i = 0; i < 4; i++)					// 행 반복
		{
			for (j = 0; j < 4; j++)				// 열 반복
			{
				if (blocks[num][i][j] == 1)
				{
					printf("■");
				}
				else
				{
					printf("  "); // □ 
				}
			}
			printf("\n");
		}
	}
}
